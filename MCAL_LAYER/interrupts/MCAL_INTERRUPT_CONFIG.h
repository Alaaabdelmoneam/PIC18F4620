#define INTERRUPT_PRIORITY_HIGH_DISABLE      (INTCONbits.GIEH = 0)

#define INTERRUPT_PRIORITY_LOW_ENABLE        (INTCONbits.GIEL = 1)
#define INTERRUPT_PRIORITY_LOW_DISABLE       (INTCONbits.GIEL = 0)


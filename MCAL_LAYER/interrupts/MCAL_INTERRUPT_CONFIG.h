#define INTERRUPT_GENERAL_FEATURE_ENABLE     (INTCONbits.GIE = 1)
#define INTERRUPT_GENERAL_FEATURE_DISABLE    (INTCONbits.GIE = 0)

#define INTERRUPT_PERIPHERAL_FEATURE_ENABLE  (INTCONbits.PEIE = 1)
#define INTERRUPT_PERIPHERAL_FEATURE_DISABLE (INTCONbits.PEIE = 0)

#endif

